//一次显示3个图标（图标为32*32）
//最左边图标的X坐标为0
//正中间图标的X坐标为48
//最右边图标的X坐标为96
//每两个图标之间的间距为16



/*
动画的原理逻辑：
		在外部的while循环里调用Menu_Aniamtion()函数
		这样就会不断地对pre_selection和target_selection进行判断
		只要pre_selection不等于target_selection（当按键按下时，target_selection的值改变）
		就会触发Menu_Aniamtion()函数里的两个if判断，判断是向右移还是向左移
		向左移和向右移是对称的，现在以向左移为例
		向左移的代码里，x_pre -= Speed;这句就使x_pre的值不断（以speed步长）递减
		在下面有个函数，OLED_ShowImage(x_pre,16,32,32,Menu_Graph[pre_selection]);
		只要pre_selection和target_selection不等，x_pre就会不断变化（递增或递减）
		直到pre_selection和target_selection相等
		这是这个 上次选项 所对应的图标就已移动到所要到达的地方
		而且在移动完成后，会完成对各参数的修改
*/


	
/*定义滑动菜单要用到的全局变量-----------------------------------------------------------------------------*/

/*全局变量，不会随任务创建删除而被创建删除，是保存在内存中的*/

	int menuFlag=1;//菜单界面的光标指示标志位
	//这个指示标志位反应的是当前选中图标的索引号

	uint8_t pre_selection;//上次选择的选项（即当前选项）
	uint8_t target_selection;//目标选项（即按下按键后要到达的选项）
	//以上两个selection指向Menu_Graph数组中各个图形的索引值
	//这个“上次和下次”都是针对 要移动 来说的，应该把每次的 静止 都看成 要移动
	
	uint8_t x_pre = 48;//上次选项的x坐标
	uint8_t Speed = 4;//速度，其实就是移动的步长
	uint8_t move_flag=0;//开始移动的标志位，1表示开始移动，0表示移动停止，初值给0

/*-----------------------------------------------------------------------------定义滑动菜单要用到的全局变量*/



/*图标滑动函数-----------------------------------------------------------------------------*/

//全部由 7 个图标
void Menu_Aniamtion(void)
{
	OLED_Clear();//先清屏
	OLED_ShowImage(42,10,44,44,Frame);//画边框
	
// #1.判断当前选项的索位号 和 目标选项的索位号的大小关系来得到整体三个图标的移动方向，本质就是修改x_pre
	if(pre_selection<target_selection)//判断是否 当前选项的索位号 小于 目标选项的索位号（即要将当前移到比当前小的地方去）
		//若是，则整体三个图标向左移动
	{
		
		x_pre -= Speed;//上次选项的x坐标（本来在中央）不断以speed的步长来减去（就是移动了）
		/*注意：只要通过改动x_pre就可以控制界面的三个图标一起动。因为界面的三个图标的x坐标分别是：x_pre-48、x_pre、x_pre+48，都与x_pre是相关的*/
		
		if(x_pre==0)//判断是否完成移动的方法：当前选项的x坐标（本来在中央48）被移动到0时，移动完成
		{
			//当前图标向左移动完成后的对各参数的修改
			pre_selection=target_selection;//这时“上次选项”的索位号就加一
			move_flag=0;//移动标志位置零
			x_pre=48;//这时“新的上次选项”（即当前）的X坐标位就变为48即正中央
		}
	}
	
	else if(pre_selection>target_selection)//判断是否 当前选项的索位号 大于 目标选项的索位号（即要将当前移到比当前大的地方去）
		//若是，则整体三个图标向右移动
	{
		
		x_pre += Speed;//上次选项（本来在中央）的x坐标不断以speed的步长来增加（就是移动了）
		/*注意：只要通过改动x_pre就可以控制界面的三个图标一起动。因为界面的三个图标的x坐标分别是：x_pre-48、x_pre、x_pre+48，都与x_pre是相关的*/
		
		if(x_pre==96)//判断是否完成移动的方法：当前选项的x坐标（本来在中央48）被移动到96时，移动完成
		{
			//当前图标向右移动完成后的对各参数的修改
			pre_selection=target_selection;//这时“上次选项”的索位号就加一
			move_flag=0;//移动标志位置零
			x_pre=48;//这时“新的上次选项”（即当前）的X坐标位就变为48即正中央
		}
	}
	
// #2.将不断被修改的x_pre来体现到图形坐标里去到OLED显示，表现出移动	
	
	/* ---上次选项 所对应的图标的移动--*/
	OLED_ShowImage(x_pre,16,32,32,Menu_Graph[pre_selection]);
	//这个就是 上次选项 所对应的图标不断移动的函数
	//因为只要pre_selection和target_selection不等
	//x_pre就会不断变化（递增或递减），直到pre_selection和target_selection相等
	//这是这个 上次选项 所对应的图标就已移动到所要到达的地方
	
	
	/*---非上次选项 所对应的图标的移动----*/
	//上次选项 的前面（左边）的图标的移动：
	//只有当pre_selection>=1时，它的前面才会有图标
	//这时三个整体移动，它前面的图标才会移动
	if(pre_selection>=1)
	{
		OLED_ShowImage(x_pre-48,16,32,32,Menu_Graph[pre_selection-1]);
	}
	//上次选项 的后面（右边）的图标的移动
	//只有当pre_selection<=4时，它的后面才会有图标
	//这时三个整体移动，它后面的图标才会移动
	if(pre_selection<=5)
	{
		OLED_ShowImage(x_pre+48,16,32,32,Menu_Graph[pre_selection+1]);
	}
	
	//上次选项 的前面第二个（左边界的左边）的图标的移动
	//只有当pre_selection>=2时，左边界的左边才会有图标
	//这时三个整体移动，左边界的左边的图标才会移动
	if(pre_selection>=2)
	{
		OLED_ShowImage(x_pre-96,16,32,32,Menu_Graph[pre_selection-2]);
	}
	
	//上次选项 的后面第二个（右边界的右边）的图标的移动
	//只有当pre_selection<=4时，右边界的右边才会有图标
	//这时三个整体移动，右边界的右边的图标才会移动
	if(pre_selection<=5)
	{
		OLED_ShowImage(x_pre+96,16,32,32,Menu_Graph[pre_selection+2]);
	}
	
	
	OLED_Update();
}

