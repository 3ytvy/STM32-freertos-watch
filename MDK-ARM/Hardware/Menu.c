int Clkflag=1;//光标指示标志位
//首页的不断取按键值并判断的函数
void FirstPage_Clock(void *argument)//注意这个函数它的返回值是int，用于判断是要跳转到哪个界面
{	
	while(1)
	{
		OLED_Clear();
		KeyNum=Key_GetNum();
			
			//Clkflag是选中哪一项的标志位，当光标选中“菜单”时，Clkflag=1，当光标选中“设置”时，Clkflag=2
			
			if(KeyNum !=0 && now_flag == 1)
			{
				key_irq = 1;
			}
			//判断按下哪个键（在亮屏情况下）
			else if(KeyNum != 0 && now_flag == 0)
			{
				//亮屏计时手动复零
				time_count_ms = 0;
				time_count_s = 0;
				
				open_time=0;
				if(KeyNum==1)//上一项
				{
					Clkflag--;
					if(Clkflag<=0)
						{
						Clkflag=2;
						}
				}
				else if(KeyNum==2)//下一项
					{
						Clkflag++;
						if(Clkflag>=3)
						{
							Clkflag=1;
						}
					}
					else if(KeyNum==3 && first_open_flag == 1)//开机初启
					{first_open_flag = 0;}
					else if(KeyNum==3 && first_open_flag == 0)
					{	
						OLED_Clear();//清屏准备跳转页面
						OLED_Update();
						//发送数据到页面管理列表
						if(Clkflag == 1)
						{
							My_Send_Task_Queue(PAGE_MENU);
							ScreenOff_Permit = 0;
						}
						else if(Clkflag == 2)
						{
							My_Send_Task_Queue(PAGE_SETTING);
							ScreenOff_Permit = 1;
						}
////////						return Clkflag;	//只在选中的这个case里返回值，这样在main函数里clkflag1得到的就只有是选中的时候才有对应返回值（1或2），其他情况不返回
				
					}
				else if(KeyNum==4)//被长按关机
				{
					HAL_GPIO_WritePin(GPIOB,GPIO_PIN_13,GPIO_PIN_RESET);
					HAL_GPIO_WritePin(GPIOB,GPIO_PIN_12,GPIO_PIN_SET);
				}
			}	
		switch(Clkflag)
		{
			case 1:
				Show_Clock_UI();//显示主页UI
				OLED_ReverseArea(3,48,16,16);//选中即反色
				OLED_Update();
				break;
			case 2:
				Show_Clock_UI();//显示主页UI
				OLED_ReverseArea(109,48,16,16);//选中即反色
				OLED_Update();
				break;
				
		}
	

	
			// RTOS 延时
    vTaskDelay(pdMS_TO_TICKS(10));
	}
}


