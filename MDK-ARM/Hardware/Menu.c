void Show_Battery(void)
{
	
	int sum=0;//在这个函数里定义，每次重新执行这个函数时候，这个变量又归零
	for(int i=0;i<3000;i++)
	{
		ADValue=AD_GetValue();
		sum+=ADValue;
	}
	ADValue=sum/3000;//这就是进行了均值滤波	
	VBAT=(float)ADValue/4095*3.3;
	
	
	Battery_Capacity=(ADValue-3296)*100/799;//乘100是因为直接转成百分比
	if(Battery_Capacity<0){Battery_Capacity=0;}
	
		
	if(Battery_Capacity==100)
	{OLED_ShowNum(85,4,Battery_Capacity,3,OLED_6X8);}
	else if(Battery_Capacity>=10)
	{OLED_ShowNum(91,4,Battery_Capacity,2,OLED_6X8);}
	else if(Battery_Capacity<10)
	{OLED_ShowNum(97,4,Battery_Capacity,1,OLED_6X8);}
	OLED_ShowChar(103,4,'%',OLED_6X8);
	
	
	
	if(Battery_Capacity==100)
	{OLED_ShowImage(110,0,16,16,battery);}
	else if(Battery_Capacity>=0)
	{
		OLED_ShowImage(110,0,16,16,battery);
		OLED_ClearArea((112+Battery_Capacity/10),5,(10-Battery_Capacity/10),6);
	}
	
}

