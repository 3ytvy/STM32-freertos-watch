/*秒表光标操作函数----------------------------------------------------------------------------*/

void stopwatch(void *argument)
{
	while(1)
	{
	
	//秒表数据更新

	if(stopwatch_update_flag == 1)
	{
		stopwatch_update_flag = 0;
		//总数自增
		sec_cen_total += 1;
	
		//总秒数转为数组里的数
		time_buf[2] = sec_cen_total % 100;        // 0.01s 位  00~99
		time_buf[1] = (sec_cen_total/100) % 60;   // 秒
		time_buf[0] = (sec_cen_total/6000) % 60;  // 分
		time_buf[3] = (sec_cen_total / 360000);    //小时
	}	
		
	OLED_Clear();
		
	uint8_t stopwatch_flag_temp=0;
	KeyNum=Key_GetNum();
		
		if(KeyNum !=0 && now_flag == 1)
		{
			key_irq = 1;
		}
		//判断按下哪个键（在亮屏情况下）
		if(KeyNum != 0 && now_flag == 0)
		{
			//亮屏计时手动复零
				time_count_ms = 0;
				time_count_s = 0;
			if(KeyNum==1)//上一项
		{
			stopwatch_flag--;
			if(stopwatch_flag<=0)
				{
				stopwatch_flag=3;
				}
		}
		else if(KeyNum==2)//下一项
			{
				stopwatch_flag++;
				if(stopwatch_flag>=4)
				{
					stopwatch_flag=1;
				}
			}
		else if(KeyNum==3)//确认
			{	
				OLED_Clear();//清屏准备跳转页面
				OLED_Update();	

				stopwatch_flag_temp=stopwatch_flag;

			}
		}
			
			
		//Temp的值的判断函数
		if(stopwatch_flag_temp==1)//第一行被按下（开始 / 计次 / 继续）
		{
			if(open_clk==0 && paulse==0)					//按下前是没计时，按下就是开始计时
			{
				open_clk=1;//计时标志位置1表示在计时
				HAL_TIM_Base_Start_IT(&htim3);//开启计数器TIM3 + 开中断
			}
			
			else if(open_clk==1 && paulse==0)		//计时过程中，不是从按了“暂停”以后开始的
//			//这里出过bug，就是这个else没写
//			//else 不写的话，一按下“开始”按键，就会先上面那个open_clk==0成立，执行了open_clk=1;
//			//导致open_clk==1，然后接下来就这个if，这个if又成立
//			//就执行下面这些
//			//造成：一按下“开始”就计了一次次00:00:00
			{
					lap_flag++;if(lap_flag>4){lap_flag=1;}
					if(lap_flag==1)
					{
						time_lap_buf0[0] = time_buf[0];
						time_lap_buf0[1] = time_buf[1];
						time_lap_buf0[2] = time_buf[2];
						time_lap_buf0[3] = time_buf[3];
					}
					else if(lap_flag==2)
					{
						time_lap_buf1[0] = time_buf[0];
						time_lap_buf1[1] = time_buf[1];
						time_lap_buf1[2] = time_buf[2];
						time_lap_buf1[3] = time_buf[3];
					}
					else if(lap_flag==3)
					{
						time_lap_buf2[0] = time_buf[0];
						time_lap_buf2[1] = time_buf[1];
						time_lap_buf2[2] = time_buf[2];
						time_lap_buf2[3] = time_buf[3];
						
					}
					else if(lap_flag==4)
					{
						time_lap_buf3[0] = time_buf[0];
						time_lap_buf3[1] = time_buf[1];
						time_lap_buf3[2] = time_buf[2];
						time_lap_buf3[3] = time_buf[3];						
					}
			}
				else if(open_clk==0 && paulse==1)//从按了“暂停”以后开始的，这时这是按下“继续”
				{
					//再次开启计时
					paulse=0;
					open_clk=1;//计时标志位置1表示在计时
					HAL_TIM_Base_Start_IT(&htim3);//开启计数器TIM3 + 开中断
					
				}
				
			}
		
		
		else if(stopwatch_flag_temp==2)//第二行被按下（暂停）
		{
			HAL_TIM_Base_Stop_IT(&htim3);//关闭计数器TIM3 + 关中断
			open_clk=0;//计时标志位置0表示没有在计时
			paulse=1;//暂停标志位置1
						
		}
		
		else if(stopwatch_flag_temp==3)//第三行被按下（退出）
		{
			HAL_TIM_Base_Stop_IT(&htim3);//关闭计数器TIM3 + 关中断
			
			//标志位和数据值和临时数据都要清零再退出
			//各种标志位
			open_clk=0;
			paulse=0;
			lap_flag=0;
			//临时数据
			time_lap_buf0[0] = 0;
			time_lap_buf0[1] = 0;
			time_lap_buf0[2] = 0;	
			time_lap_buf1[0] = 0;
			time_lap_buf1[1] = 0;
			time_lap_buf1[2] = 0;	
			time_lap_buf2[0] = 0;
			time_lap_buf2[1] = 0;
			time_lap_buf2[2] = 0;
			time_lap_buf3[0] = 0;
			time_lap_buf3[1] = 0;
			time_lap_buf3[2] = 0;
			
			time_lap_buf0[3] = 0;
			time_lap_buf1[3] = 0;
			time_lap_buf2[3] = 0;
			time_lap_buf3[3] = 0;
			//保存的计时数据
			time_buf[0] = 0;
			time_buf[1] = 0;
			time_buf[2] = 0;
			time_buf[3] = 0;			
			sec_cen_total=0;
			
			stopwatch_flag = 1;//光标指示标志位给它置一（初始情况），以后再进入就是初始情况
			
			//给页面管理队列写数据
			My_Send_Task_Queue(PAGE_MENU);
			ScreenOff_Permit = 0;
		}
			
			
			
			
		//判断光标
		if(stopwatch_flag==1)//光标停在了第一行
		{
			if(open_clk==0)//没有计时
			{
				stopwatch_UI();
				OLED_ReverseArea(88,5,32,16);
				OLED_Update();
			}
			if(open_clk==1)//正在计时
			{
				stopwatch_UI();
				OLED_ReverseArea(88,5,32,16);
				OLED_Update();
			}
		}
		else if(stopwatch_flag==2)//光标停在了第一行
		{
				stopwatch_UI();
				OLED_ReverseArea(88,23,32,16);
				OLED_Update();
		}
		else if(stopwatch_flag==3)//光标停在了第三行
		{
				stopwatch_UI();
				OLED_ReverseArea(88,41,32,16);
				OLED_Update();
		}
	
		//RTOS延时
		vTaskDelay(pdMS_TO_TICKS(10));
}
}

