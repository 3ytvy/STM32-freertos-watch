/*音乐进度条-------------------------------------------------------------------------------*/


void Music_ProgressBar(void)
{
	// 固定配置
	const uint8_t x_start = 2;
	const uint8_t y_pos   = 58;
	const uint8_t x_end   = 126;
	const uint8_t height  = 4;
	
	
	/*==============计算进度===============*/
	// 总长度
	int total_width = x_end - x_start;

	// 当前系统Tick数
	uint32_t now_tick = xTaskGetTickCount();
	
	// 当前实际累积到的播放时长
	uint32_t real_ms;

	if(music_state == 2)  // 暂停 → 进度条固定不动
	{
		real_ms = (music_pause_tick - music_start_tick) * portTICK_PERIOD_MS	- music_paused_time;
	}
	else  // 播放 → 正常走
	{
		real_ms = (now_tick - music_start_tick) * portTICK_PERIOD_MS - music_paused_time;
	}
	
	// 边界保护
	if(real_ms > music_total_time) real_ms = music_total_time;

	music_process = (float)real_ms / music_total_time;
	
	
	/*=============显示进度条==============*/
	// 当前进度条宽度
	int bar_width = (int)(total_width * music_process + 0.5f);//加个+ 0.5f，四舍五入，因为int是取这个数字（带小数）的整数部分，所以是比这个数字小的整数
	
	// 边界保护
	if(bar_width < 0) bar_width = 0;
	if(bar_width > total_width) bar_width = total_width;
    
	// 显示进度条
	OLED_ReverseArea(2,58,bar_width,4);
	
	
}

