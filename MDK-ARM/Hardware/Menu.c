/*设置蜂鸣器频率（TIM4_CH1 PB6）-------------------------------------------------------------------------------*/

void Buzzer_SetFreq(uint16_t freq)
{
	if(freq == 0)
	{
		 // 静音：占空比设为0，停止发声
		__HAL_TIM_SET_COMPARE(&htim4,TIM_CHANNEL_1,0);
		HAL_TIM_PWM_Stop(&htim4,TIM_CHANNEL_1);
		return;
	}
	
	
	/*通过修改ARR（自动重装载值）来调整频率
		频率（freq） = 64MHz（计数器的时钟来源频率） / PSC（分频系数） / ARR（自动重装载值
		
		根据目标频率计算新的自动重装载值ARR（所以不用修改PSC，置改ARR）
		64MHz（计数器的时钟来源频率） / PSC（分频系数） = 1MHz
		所以 1MHz / freq 就是一个周期的计数次数
		这样得到目的freq
	*/
	uint16_t arr = 1000000 / freq;
	
	//更新ARR和比较值（要保持50%占空比，这个是决定音量的）
	__HAL_TIM_SET_AUTORELOAD(&htim4,arr - 1);
	__HAL_TIM_SET_COMPARE(&htim4,TIM_CHANNEL_1,arr / 2);	
}

