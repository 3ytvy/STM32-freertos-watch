int Setflag=1;//这个是设置界面的光标位置指示位
//这个函数是在这个设置界面里操作光标
void SettingPage(void *argument)
{
	
	
	while(1)
	{
	OLED_Clear();
		
	uint8_t SetFlag_Temp=0;//这个临时变量是用来存放在设置界面光标的指示位的
	//使用临时变量而不用SetFlag的原因是：
	//SetFlag并不能反应按下了“确认”，它指示的是光标选中了哪个选项
	//所以在 按下“确认” 的时候把SetFlag的值赋给临时变量SetFlag_Temp
	//那么SetFlag_Temp就既能说明 已经按下了“确认”，并且它的值指示了是光标选中了哪个选项
		
	KeyNum=Key_GetNum();
		if(KeyNum !=0 && now_flag == 1)
		{
			key_irq = 1;
		}
	//判断按下哪个键（在亮屏情况下）
		if(KeyNum != 0 && now_flag == 0)
		{
			//亮屏计时手动复零
				time_count_ms = 0;
				time_count_s = 0;
		if(KeyNum==1)//上一项
		{
			Setflag--;
			if(Setflag<=0)
				{
				Setflag=2;
				}
		}
		else if(KeyNum==2)//下一项
			{
				Setflag++;
				if(Setflag>=3)
				{
					Setflag=1;
				}
			}
		else if(KeyNum==3)//确认
			{	
				OLED_Clear();//清屏准备跳转页面
				OLED_Update();	
				//return Setflag;	//只在选中的这个case里返回值，这样在main函数里clkflag1得到的就只有是选中的时候才有对应返回值（1或2），其他情况不返回
				//本来是return返回一个值，这里用变量SetFlag_Temp来存放，然后判断这个变量
				SetFlag_Temp=Setflag;
				//当按下“确认”按键时，给SetFlag_Temp赋值，指示现在选中的是哪个
				//逻辑：如果没按这个“确认”按键，SetFlag_Temp的值是0，说明未按下安静
				//		如果按下了“确认”按键，SetFlag_Temp就有值，而且其值指示现在选中的是哪个
				//		所以在下面加上对SetFlag_Temp的值的判断函数
				//注意这个SetFlag_Temp要清零
			}
		}
			
		//对SetFlag_Temp的值的判断函数
		if(SetFlag_Temp==1)//说明选中的是返回键并且按下了
		{
			back_to_home_flag = 1;
			My_Send_Task_Queue(PAGE_HOME);
			ScreenOff_Permit = 0;
		}
		else if(SetFlag_Temp==2)//说明选中的是“日期时间设置”并且按下了
		{
			OLED_Clear();//先清屏
			/*向页面管理队列写数据*/
			My_Send_Task_Queue(PAGE_SETTIME);
			ScreenOff_Permit = 1;
			
		}
			
	switch(Setflag)
	{
		case 1:
			Show_SettingPage_UI();
			OLED_ReverseArea(0,0,16,16);//选中即反色
			OLED_Update();
			break;
		case 2:
			Show_SettingPage_UI();
			OLED_ReverseArea(0,16,96,16);//选中即反色
			OLED_Update();
			break;
}
		//RTOS延时
		vTaskDelay(pdMS_TO_TICKS(10));
	}
}

