#ifndef __MPU6050_REG_H
#define __MPU6050_REG_H
//7位地址0x68
//8位地址0xD0
#define MPU6050_ADDRESS		0xD0		//MPU6050的I2C从机地址

#define	MPU6050_SMPLRT_DIV		0x19
#define	MPU6050_CONFIG			0x1A
#define	MPU6050_GYRO_CONFIG		0x1B
#define	MPU6050_ACCEL_CONFIG	0x1C //加速度计配置

#define	MPU6050_ACCEL_XOUT_H	0x3B
#define	MPU6050_ACCEL_XOUT_L	0x3C
#define	MPU6050_ACCEL_YOUT_H	0x3D
#define	MPU6050_ACCEL_YOUT_L	0x3E
#define	MPU6050_ACCEL_ZOUT_H	0x3F
#define	MPU6050_ACCEL_ZOUT_L	0x40
#define	MPU6050_TEMP_OUT_H		0x41
#define	MPU6050_TEMP_OUT_L		0x42
#define	MPU6050_GYRO_XOUT_H		0x43
#define	MPU6050_GYRO_XOUT_L		0x44
#define	MPU6050_GYRO_YOUT_H		0x45
#define	MPU6050_GYRO_YOUT_L		0x46
#define	MPU6050_GYRO_ZOUT_H		0x47
#define	MPU6050_GYRO_ZOUT_L		0x48

#define	MPU6050_PWR_MGMT_1		0x6B
#define	MPU6050_PWR_MGMT_2		0x6C
#define	MPU6050_WHO_AM_I		0x75

#define MPU6050_MOT_THR 0x1F //运动阈值寄存器 
#define MPU6050_MOT_DUR 0x20 //运动持续时间寄存器
#define MPU6050_INT_ENABLE 0x38 //中断使能寄存器
#define MPU6050_INT_PIN_CFG 0x37 //中断引脚配置寄存器
#define MPU6050_INT_STATUS 0x3A //中断状态寄存器

#define MPU6050_SMPLRT_DIV 0x19 //采样率分频设置寄存器
#define MPU6050_CONFIG 0x1A //用于配置要去采样哪些数据的寄存器

#define MPU6050_USER_CTRL 0x6A//用户控制寄存器，里面有一位是开启FIFO用的

#define MPU6050_FIFO_EN 0x23 //配置FIFO使能


#define MPU6050_FIFO_COUNT_H 0x72
#define MPU6050_FIFO_COUNT_L 0x73

#define MPU6050_SIGNAL_PATH_RESET 0x68


#endif
